package first.com.model;

public class BfileDTO {
	
	private int bfile_id;
	private int bcode_id;
	private String filename;

	
	
	public int getBfile_id() {
		return bfile_id;
	}

	public void setBfile_id(int bfile_id) {
		this.bfile_id = bfile_id;
	}

	public int getBcode_id() {
		return bcode_id;
	}

	public void setBcode_id(int bcode_id) {
		this.bcode_id = bcode_id;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}
	

}
